export default function Product() {
  const {product} = useLoaderData<typeof loader>();

  // Optimistically selects a variant with given available variant information
  const selectedVariant = useOptimisticVariant(
    product.selectedOrFirstAvailableVariant,
    getAdjacentAndFirstAvailableVariants(product),
  );

  // Sets the search param to the selected variant without navigation
  // only when no search params are set in the url
  useSelectedOptionInUrlParam(selectedVariant.selectedOptions);

  // Get the product options array
  const productOptions = getProductOptions({
    ...product,
    selectedOrFirstAvailableVariant: selectedVariant,
  });

  const {title, descriptionHtml, productType} = product;

  // Build the gallery from the product's images, with the selected variant's
  // image promoted to the front so it always leads. De-duplicate by id.
  const galleryImages = (() => {
    const nodes = product.images?.nodes ?? [];
    const lead = selectedVariant?.image;
    const ordered = lead
      ? [lead, ...nodes.filter((n) => n.id !== lead.id)]
      : nodes;
    return ordered.slice(0, 5);
  })();

  return (
    <div className="product-detail">
      <div className="wrap product-detail-grid">
        <div className="product-gallery">
          {galleryImages.length > 0 ? (
            galleryImages.map((image, i) => (
              <div className="pg-frame" key={image.id}>
                <Image
                  alt={image.altText || title}
                  data={image}
                  aspectRatio="3/4"
                  sizes="(min-width: 980px) 46vw, 100vw"
                  loading={i === 0 ? 'eager' : 'lazy'}
                />
              </div>
            ))
          ) : (
            <div className="pg-frame">
              <ProductImage image={selectedVariant?.image} />
            </div>
          )}
        </div>

        <div className="product-main">
          {productType ? (
            <span className="product-cat eyebrow">{productType}</span>
          ) : null}
          <h1 className="display">{title}</h1>
          <ProductPrice
            price={selectedVariant?.price}
            compareAtPrice={selectedVariant?.compareAtPrice}
          />
          <ProductForm
            productOptions={productOptions}
            selectedVariant={selectedVariant}
          />
          {descriptionHtml ? (
            <div className="product-description">
              <p
                className="eyebrow"
                data-pt="Descrição"
                data-en="Description"
              >
                Descrição
              </p>
              <div
                className="product-description-body"
                dangerouslySetInnerHTML={{__html: descriptionHtml}}
              />
            </div>
          ) : null}
        </div>
      </div>
      <Analytics.ProductView
        data={{
          products: [
            {
              id: product.id,
              title: product.title,
              price: selectedVariant?.price.amount || '0',
              vendor: product.vendor,
              variantId: selectedVariant?.id || '',
              variantTitle: selectedVariant?.title || '',
              quantity: 1,
            },
          ],
        }}
      />
    </div>
  );
}

